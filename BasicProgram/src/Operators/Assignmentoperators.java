package Operators;

public class Assignmentoperators {

	public static void main(String[] args) {
		//assignment operators
		int a=2345;
		System.out.println("addition:"+(a+=8));
		System.out.println("divide:"+(a/=3));
		System.out.println("modulus:"+(a%=5));
		System.out.println("multiply:"+(a*=5));
		

	}

}
